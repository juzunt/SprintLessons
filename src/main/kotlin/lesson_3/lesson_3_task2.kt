package org.example.lesson_3

fun main() {
    val firstName = "Татьяна"
    var lastName = "Андреева"
    val middleName = "Сергеевна"
    var age = 20

    println("$lastName $firstName $middleName, $age лет")

    lastName = "Сидорова"
    age = 22

    println("$lastName $firstName $middleName, $age года")
}